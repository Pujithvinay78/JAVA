
import java.util.*;

class main{
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();

        switch(n){
            case 1:
                System.out.println("this is 1");
                break;

            case 2: 
            System.out.println("this is 2");
            break;

            default:
                System.out.println("none");
                break;       }
    }
}